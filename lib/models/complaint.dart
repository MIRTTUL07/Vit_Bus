class Complaint {
  final String id;
  final String type;
  final String issue;
  final String busNumber;
  final String status;
  final String date;
  final String description;

  Complaint({
    required this.id,
    required this.type,
    required this.issue,
    required this.busNumber,
    required this.status,
    required this.date,
    required this.description,
  });
}
